package model;

/**
 *
 * @author Will
 */
public class Disco {

    /**
     * @return the idDisco
     */
    
    private int idDisco;
    private int Espaco;
    private String Marca;
    private int N_Disco;
    private int Maquina_Disco;
    
    public int getIdDisco() {
        return idDisco;
    }

    /**
     * @param idDisco the idDisco to set
     */
    public void setIdDisco(int idDisco) {
        this.idDisco = idDisco;
    }

    /**
     * @return the Espaco
     */
    public int getEspaco() {
        return Espaco;
    }

    /**
     * @param Espaco the Espaco to set
     */
    public void setEspaco(int Espaco) {
        this.Espaco = Espaco;
    }

    /**
     * @return the Marca
     */
    public String getMarca() {
        return Marca;
    }

    /**
     * @param Marca the Marca to set
     */
    public void setMarca(String Marca) {
        this.Marca = Marca;
    }

    /**
     * @return the N_Disco
     */
    public int getN_Disco() {
        return N_Disco;
    }

    /**
     * @param N_Disco the N_Disco to set
     */
    public void setN_Disco(int N_Disco) {
        this.N_Disco = N_Disco;
    }

    /**
     * @return the Maquina_Disco
     */
    public int getMaquina_Disco() {
        return Maquina_Disco;
    }

    /**
     * @param Maquina_Disco the Maquina_Disco to set
     */
    public void setMaquina_Disco(int Maquina_Disco) {
        this.Maquina_Disco = Maquina_Disco;
    }

   
}
