public class Acesso {
    private int idAcesso;
    private String Descricao_Acesso;
    private String Nome_Acesso;

    /**
     * @return the idAcesso
     */
    public int getIdAcesso() {
        return idAcesso;
    }

    /**
     * @param idAcesso the idAcesso to set
     */
    public void setIdAcesso(int idAcesso) {
        this.idAcesso = idAcesso;
    }

    /**
     * @return the Descricao_Acesso
     */
    public String getDescricao_Acesso() {
        return Descricao_Acesso;
    }

    /**
     * @param Descricao_Acesso the Descricao_Acesso to set
     */
    public void setDescricao_Acesso(String Descricao_Acesso) {
        this.Descricao_Acesso = Descricao_Acesso;
    }

    /**
     * @return the Nome_Acesso
     */
    public String getNome_Acesso() {
        return Nome_Acesso;
    }

    /**
     * @param Nome_Acesso the Nome_Acesso to set
     */
    public void setNome_Acesso(String Nome_Acesso) {
        this.Nome_Acesso = Nome_Acesso;
    }
    
    
}
